DROP DATABASE IF EXISTS vrapi_pro;
CREATE DATABASE vrapi_pro;

USE vrapi_pro;

CREATE TABLE Users (
	UserId INTEGER NOT NULL auto_increment,
	Name VARCHAR(255),
	Surname VARCHAR(255),
	Username VARCHAR(255),
	Pwd VARCHAR(255),
	Telf INTEGER,
	UserMail VARCHAR(255),
    JoinDate datetime,
	PRIMARY KEY (UserId)
);

CREATE TABLE SesionUsers(
    UserId INTEGER NOT NULL,
    Token VARCHAR(999),
    ExpirationDate datetime,
    FOREIGN KEY (UserId) REFERENCES Users (UserId),
    PRIMARY KEY (UserId)
);

CREATE TABLE Experience (
	ExperienceId INTEGER NOT NULL auto_increment,
	Name VARCHAR(255),
	Description VARCHAR(555),
	Rating INTEGER,
	PRIMARY KEY (ExperienceId)
);

CREATE TABLE Images (
	ImageId INTEGER NOT NULL auto_increment,
	Src VARCHAR(255),
	PRIMARY KEY (ImageId)
);

CREATE TABLE Category (
	CategoryId INTEGER NOT NULL auto_increment,
	Name VARCHAR(255),
	PRIMARY KEY (CategoryId)
);

CREATE TABLE Material (
	MaterialId INTEGER NOT NULL auto_increment,
	Name VARCHAR(255),
	Description VARCHAR(999),
	VRType INTEGER NOT NULL,
    Icon VARCHAR(999),
    IsHeadset INTEGER NOT NULL,
	PRIMARY KEY (MaterialId)
);

CREATE TABLE Components (
	ComponentId INTEGER NOT NULL auto_increment,
	Name VARCHAR(255),
	MaterialId INTEGER,
    ComponentStatus enum('BUEN_ESTADO', 'MAL_ESTADO', 'NO_ESTA'),
	PRIMARY KEY (ComponentId)
);

CREATE TABLE Procedures (
    ProcedureId INTEGER NOT NULL PRIMARY KEY,
    Title VARCHAR(255),
    Description VARCHAR(255),
    Src VARCHAR(255),
    ComponentId INTEGER,
    MaterialId INTEGER
);	

CREATE TABLE Register (
	RegisterId INTEGER NOT NULL auto_increment,
	MaterialId INTEGER,
    RegisterDate datetime,
    UserId INTEGER,
    Description VARCHAR(999),
    RegisterStatus INTEGER,
	IsClosed INTEGER,
	PRIMARY KEY (RegisterId)
);

CREATE TABLE Invalidations (
	InvalidationId INTEGER NOT NULL auto_increment,
    UserId INTEGER NOT NULL,
    RegisterId INTEGER NOT NULL,
    Reason VARCHAR(999),
    InvalidationDate datetime NOT NULL,
    PRIMARY KEY (InvalidationId)
);

CREATE TABLE Register_Material (
	RegisterId INTEGER,
	MaterialId INTEGER,
	FOREIGN KEY (RegisterId) REFERENCES Register (RegisterId),
	FOREIGN KEY (MaterialId) REFERENCES Material (MaterialId),
	PRIMARY KEY (RegisterId, MaterialId)
);

CREATE TABLE Register_Procedures (
	RegisterId INTEGER,
	ProcedureId INTEGER,
	FOREIGN KEY (RegisterId) REFERENCES Register (RegisterId),
	FOREIGN KEY (ProcedureId) REFERENCES Procedures (ProcedureId),
	PRIMARY KEY (RegisterId, ProcedureId)
);

CREATE TABLE Component_Procedures (
	ProcedureId INTEGER,
	ComponentId INTEGER,
	FOREIGN KEY (ComponentId) REFERENCES Components (ComponentId),
	FOREIGN KEY (ProcedureId) REFERENCES Procedures (ProcedureId),
	PRIMARY KEY (ComponentId, ProcedureId)
);

CREATE TABLE Experience_Category (
	ExperienceId INTEGER,
	CategoryId INTEGER,
	FOREIGN KEY (ExperienceId) REFERENCES Experience (ExperienceId),
	FOREIGN KEY (CategoryId) REFERENCES Category (CategoryId),
	PRIMARY KEY (ExperienceId, CategoryId)
);

CREATE TABLE Experience_Images (
	ExperienceId INTEGER,
	ImageId INTEGER,
	FOREIGN KEY (ExperienceId) REFERENCES Experience (ExperienceId),
	FOREIGN KEY (ImageId) REFERENCES Images (ImageId),
	PRIMARY KEY (ExperienceId, ImageId)
);

CREATE TABLE Experience_Material (
  ExperienceId INTEGER,
  MaterialId INTEGER,
  FOREIGN KEY (ExperienceId) REFERENCES Experience (ExperienceId),
  FOREIGN KEY (MaterialId) REFERENCES Material (MaterialId),
  PRIMARY KEY (ExperienceId, MaterialId)
);

CREATE TABLE Material_Components (
	MaterialId INTEGER,
	ComponentId INTEGER,
	FOREIGN KEY (MaterialId) REFERENCES Material (MaterialId),
	FOREIGN KEY (ComponentId) REFERENCES Components (ComponentId),
	PRIMARY KEY (MaterialId, ComponentId)
);

CREATE TABLE Material_Images (
	MaterialId INTEGER,
	ImageId INTEGER,
	FOREIGN KEY (MaterialId) REFERENCES Material (MaterialId),
	FOREIGN KEY (ImageId) REFERENCES Images (ImageId),
	PRIMARY KEY (MaterialId, ImageId)
);

CREATE TABLE News (
	NewId INTEGER,
	Title VARCHAR(999),
    Subtitle VARCHAR(999),
    Body VARCHAR(999),
    PublishDate datetime,
	PRIMARY KEY (NewId)
);

CREATE TABLE Events (
	EventId INTEGER,
    Name VARCHAR(999),
    Date datetime,
	QR VARCHAR(999),
    PRIMARY KEY(EventId)
);

CREATE TABLE Crate (
	CrateId VARCHAR(100),
    PRIMARY KEY(CrateId)
);

CREATE TABLE Crate_Components(
	CrateId VARCHAR(100),
    ComponentId INTEGER,
    PRIMARY KEY(CrateId, ComponentId)
);

-- Projects

CREATE TABLE Hospital_Players (
    sessionId VARCHAR(100) NOT NULL,
    sessionStartDate datetime NOT NULL,
    sessionEndDate datetime,
    PRIMARY KEY(sessionId)
);

CREATE TABLE Hospital_Checkpoints (
    sessionId VARCHAR(100) NOT NULL,
    checkpointId INTEGER NOT NULL,
    date datetime NOT NULL
);

CREATE TABLE IF NOT EXISTS Player_Last_Checkpoints (
  sessionId VARCHAR(255) PRIMARY KEY,
  checkpointId INT,
  date DATETIME
);


